/*There are seven different data types in JavaScript.
1. Numbers - integers or numbers with decimal points
2. String - any grouping of characters inside single quotes ' ' or double quotes " "
3. Boolean - true or false
4. Null - the intentional absence of a value using the keyword null
5. Undefined - the keyword undefined is also the absence of a value but with a different use than null
6. Symbol - a unique identifier that is newer to the language
7. Object - a collection of related data
The first six types are called primitive data, while objects are much more complex. */
